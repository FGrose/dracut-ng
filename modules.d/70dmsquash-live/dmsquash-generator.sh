#!/bin/sh
# dmsquash-generator

command -v get_rd_overlay > /dev/null || . /lib/overlayfs-lib.sh

[ "$root" ] || root=$(getarg root=)
# support legacy syntax of passing rd.live.image and then just the base root
if getargbool 0 rd.live.image; then
    liveroot=live:"$root"
fi
if [ "${root%%:*}" = live ]; then
    liveroot=$root
fi
[ "${liveroot%%:*}" = live ] || exit 0
case "$liveroot" in
    live:LABEL=* | LABEL=* | live:UUID=* | UUID=* | live:PARTUUID=* | PARTUUID=* | live:PARTLABEL=* | PARTLABEL=*)
        root="live:$(label_uuid_to_dev "${root#live:}")"
        rootok=1
        ;;
    live:CDLABEL=* | CDLABEL=*)
        root="${root#live:}"
        root="$(echo "$root" | sed 's,/,\\x2f,g;s, ,\\x20,g')"
        root=live:/dev/disk/by-label/"${root#CDLABEL=}"
        rootok=1
        ;;
    live:/*.[Ii][Ss][Oo] | /*.[Ii][Ss][Oo])
        root="${root#live:}"
        root=liveiso:"${root}"
        rootok=1
        ;;
    live:/dev/*)
        rootok=1
        ;;
    live:/*.[Ii][Mm][Gg] | /*.[Ii][Mm][Gg])
        [ -f "${root#live:}" ] && rootok=1
        ;;
esac
getarg live.updates= && {
    case "$liveroot" in
        live:nfs://* | nfs://*)
            root="${root#live:}"
            rootok=1
            ;;
        live:http://* | http://*)
            root="${root#live:}"
            rootok=1
            ;;
        live:https://* | https://*)
            root="${root#live:}"
            rootok=1
            ;;
        live:ftp://* | ftp://*)
            root="${root#live:}"
            rootok=1
            ;;
        live:torrent://* | torrent://*)
            root="${root#live:}"
            rootok=1
            ;;
        live:tftp://* | tftp://*)
            root="${root#live:}"
            rootok=1
            ;;
    esac
}
[ "$rootok" ] || exit 0

GENERATOR_DIR="$2"
[ "$GENERATOR_DIR" ] || exit 1
[ -d "$GENERATOR_DIR" ] || mkdir -p "$GENERATOR_DIR"

command -v get_rd_overlay > /dev/null || . /lib/overlayfs-lib.sh
generator=generator
# Set p_pt, p_ptfstype, size; toggle volatile.
get_rd_overlay LiveOS_rootfs

rfstype="$(getarg rootfstype=)"
rflags="$(getarg rootflags=)"

get_rd_overlay LiveOS_rootfs

echo "# Automatically generated by ${0##*/}
[Unit]
DefaultDependencies=no
Before=initrd-root-fs.target
[Mount]
Where=/run/initramfs/live
What=/run/initramfs/livedev
Options=ro
Type=iso9660" > "$GENERATOR_DIR"/run-initramfs-live.mount

    mkdir -p "$GENERATOR_DIR"/run-initramfs-live.device.d
    echo "# Automatically generated by ${0##*/}
[Unit]
JobTimeoutSec=3000
JobRunningTimeoutSec=3000" > "$GENERATOR_DIR"/run-initramfs-live.device.d/timeout.conf

echo "# Automatically generated by ${0##*/}
[Unit]
DefaultDependencies=no
Before=initrd-root-fs.target
[Mount]
Where=/run/initramfs/ESP
What=/run/initramfs/espdev
Options=nocase,shortname=win95
Type=vfat" > "$GENERATOR_DIR"/run-initramfs-ESP.mount

    mkdir -p "$GENERATOR_DIR"/run-initramfs-ESP.device.d
    echo "# Automatically generated by ${0##*/}
[Unit]
JobTimeoutSec=3000
JobRunningTimeoutSec=3000" > "$GENERATOR_DIR"/run-initramfs-ESP.device.d/timeout.conf

if [ "$OverlayFS" ]; then
    overlayfs_mount_generator
else
    echo "# Automatically generated by ${0##*/}
[Unit]
DefaultDependencies=no
Before=initrd-root-fs.target
[Mount]
Where=/sysroot
What=/dev/mapper/live-rw
Options=${rflags}
Type=${rfstype-auto}" > "$GENERATOR_DIR"/sysroot.mount

    mkdir -p "$GENERATOR_DIR"/dev-mapper-live\x2drw.device.d
    echo "# Automatically generated by ${0##*/}
[Unit]
JobTimeoutSec=3000
JobRunningTimeoutSec=3000" > "$GENERATOR_DIR"/dev-mapper-live\x2drw.device.d/timeout.conf
fi
